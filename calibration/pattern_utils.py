def prepare_gray_pattern(intensity_uint8, size=[768, 1024]):
    blank_image = np.zeros(size, np.uint8)
    blank_image[:] = intensity_uint8
    pattern = cv2.cvtColor(blank_image, cv2.COLOR_GRAY2BGR)

    return pattern
    

